    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
       try{
            this.account.setValue(Double.valueOf(this.jtxt_account.getText()));
            if(this.jr_10.isSelected()){
                this.account.setTip(10);
            }else if(this.jr_15.isSelected()){
                this.account.setTip(15);
            }else{
                this.account.setTip(20);
            }
            this.account.setPeople(Integer.valueOf(this.jtxt_people.getText()));
            this.jtxt_result.setText(String.format("%.2f",this.account.getTotallyByPerson()));
        }catch(Exception exp){
           JOptionPane.showMessageDialog(this, "Aconteceu um error:\n"+exp.getMessage());
       }
    }//GEN-LAST:event_btn_calcularActionPerformed

